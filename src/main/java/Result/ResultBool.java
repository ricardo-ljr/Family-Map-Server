package Result;

public class ResultBool {
    boolean success;

    /**
     * Returns true or false
     *
     * @return
     */
    public boolean isSuccess() {
        return success;
    }

    /**
     * Set success to true or false
     *
     * @param success
     */
    public void setSuccess(boolean success) {
        this.success = success;
    }
}
